set1 = {2,3,4,5,6}
set2 = {2,3,4,7,8}


# union- it compares the values in both set and gives the unique values in both set.

# as in the set the values are not repeated so it always give the unique values.

print(set1.union(set2))


# intersection- in this method both the sets are compared and overlapping values in the both sets returns.

# values present in both the sets it return.

print(set1.intersection(set2))
